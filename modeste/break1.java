class breakExamlpe {
    public static void main(String[] args) {
      
        // for loop
        for (int y = 1; y <= 10; ++y) {

            // if the value of i is 5 the loop terminates  
            if (y == 7) {
                break;
            }      
            System.out.println(y);
        }   
    }
}