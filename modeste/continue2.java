 class ContinueExample 
{  
public static void main(String[] args) {  
    //for loop  
    for(int x=1;x<=6;x++){  
        if(x==5){  
              
            continue;//it will skip the rest statement  
        }  
        System.out.println("I'm Modeste NIYONKURU");  
    }  
}  
}  